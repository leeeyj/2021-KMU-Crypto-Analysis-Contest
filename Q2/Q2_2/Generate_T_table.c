uint32 T1[256];
uint32 T2[256];
uint32 T3[256];
uint32 T4[256];

uint8 xtime(uint8 a)
{   
    if (a >= 128){
        a = (a << 1) ^ 0x1b; 
        return a;
    }

    a = (a << 1); 
    return a;
}

uint8 m02(uint8 a)
{
    return xtime(a);
}

uint8 m03(uint8 a)
{
    uint8 b = a;
    return m02(a) ^ b;
}

uint32 Table1(uint8 x)
{
    uint32 a = 0;
    a = m02(x) << 24 ^ x << 16 ^ x << 8 ^ m03(x);
    return a;
}

uint32 Table2(uint8 x)
{
    uint32 a = 0;
    a = m03(x) << 24 ^ m02(x) << 16 ^ x << 8 ^ x;
    return a;
}

uint32 Table3(uint8 x)
{
    uint32 a = 0;
    a = x << 24 ^ m03(x) << 16 ^ m02(x) << 8 ^ x;
    return a;
}

uint32 Table4(uint8 x)
{
    uint32 a = 0;
    a = x << 24 ^ x << 16 ^ m03(x) << 8 ^ m02(x);
    return a;
}

