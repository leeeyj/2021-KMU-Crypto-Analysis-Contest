const uint8 RC[10] = { 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

uint8 xtime(uint8 a)
{   
    if (a >= 128){
        a = (a << 1) ^ 0x1b; 
        return a;
    }

    a = (a << 1); 
    return a;
}

uint8 m02(uint8 a)
{
    return xtime(a);
}

uint8 m03(uint8 a)
{
    uint8 b = a;
    return m02(a) ^ b;
}

void Rot(uint8* t)
{
    uint8 temp = t[0];
    t[0] = t[1];
    t[1] = t[2];
    t[2] = t[3];
    t[3] = temp;
    temp = 0;
}

void Sub(uint8* t)
{
    t[0] = SBox[t[0]];
    t[1] = SBox[t[1]];
    t[2] = SBox[t[2]];
    t[3] = SBox[t[3]];
}

void block2state(uint8* block, uint8 (*in_state)[4])
{
    for (uint8 i = 0; i < 4; i++){
        for (uint8 j = 0; j < 4; j++){
            in_state[j][i] = block[i * 4 + j];
        }
    }
}

void state2block(uint8 (*state)[4], uint8* block)
{
    for (uint8 i = 0; i < 4; i++){
        for (uint8 j = 0; j < 4; j++){
            block[i * 4 + j] = state[j][i];
        }
    }
}

void AddRoundKey(uint8 (*P)[4], uint8 (*key)[4])
{
    for (uint8 i = 0; i < 4; i++){
       for (uint8 j = 0; j < 4; j++){
           P[i][j] = P[i][j] ^ key[i][j];
       }
    }
}

void SubBytes(uint8 (*P)[4])
{
    for (uint8 i = 0; i < 4; i++){
       for (uint8 j = 0; j < 4; j++){
           P[i][j] = SBox[P[i][j]]; 
       }
    }
}

void ShiftRows(uint8 (*P)[4])
{   
    for (uint8 i = 0; i < 4; i++){
        for (uint8 j = 0; j < i; j++){
            uint8 a = P[i][0];
            P[i][0] = P[i][1];
            P[i][1] = P[i][2];
            P[i][2] = P[i][3];
            P[i][3] = a;
            a = 0;
        }
    }
}

void MixColumns(uint8 (*P)[4])
{
    for (uint8 i = 0; i < 4; i++){
        uint8 a = P[0][i]; uint8 b = P[1][i];
        uint8 c = P[2][i]; uint8 d = P[3][i];

        P[0][i] = m02(a) ^ m03(b) ^ c ^ d;
        P[1][i] = a ^ m02(b) ^ m03(c) ^ d;
        P[2][i] = a ^ b ^ m02(c) ^ m03(d);
        P[3][i] = m03(a) ^ b ^ c ^ m02(d);
    
        a = 0; b = 0; c = 0; d =0;
    }
}

void KeyExpansion(uint8 *key, uint8 (*rk)[4][4])
{   
    uint8 t[4]; 
    for (uint8 i = 0; i < 4; i++){
        for (uint8 j = 0; j < 4; j++){
            rk[0][j][i] = key[i * 4 + j];
            if (i == 3){
                t[j] = key[i * 4 + j];
            }
        }
    }

    for (uint8 i = 4; i < 44; i++){
        if (i % 4 == 0){
            Rot(t);
            Sub(t);
            t[0] ^= RC[(i / 4) - 1];
            t[1] ^= 0x00;
            t[2] ^= 0x00;
            t[3] ^= 0x00;
        }
    
        for (uint8 j = 0; j < 4; j++){
            rk[i / 4][j][(i - 4) % 4] = rk[(i / 4) - 1][j][(i - 4) % 4] ^ t[j];
            t[j] = rk[i / 4][j][(i - 4) % 4];
        }
    }

    memset(t, 0, 4 * sizeof(uint8));

}

